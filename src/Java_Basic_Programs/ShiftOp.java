package Java_Basic_Programs;

public class ShiftOp {
    public static void main(String [] args) {
        int i=100;
        int j=4;
        int k=-3;
        int l=5;
        System.out.println(i<<j);//1600
        System.out.println(i>>j);//6
        System.out.println(k>>>l);//134217727
    }
}
